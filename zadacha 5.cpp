#include <iostream>
#include <stdio.h>

using namespace std;

int main()
{
	
	for (int A = 1; A < 10; A++)
	{
		for (int B = 1; B < 10; B++)
		{
			for (int C = 1; C < 10; C++)
			{
				for (int x = 0; x < 10; x++)
				{
					for (int y = 0; y < 10; y++)
					{
						if ((A * 100 + x * 10 + y) + (B * 100 + x * 10 + y) == 8 * (C * 100 + x * 10 + y)) {
							cout << A*100+x*10+y << " + " << B * 100 + x * 10 + y<<" = " << 8*(C * 100 + x * 10 + y) << "\n";
						
						}
					}
				}
			}
		}
	}


	
}
